package Bens;

public class Cliente {

	int codigo;
	int data;
	Pessoa pessoa;
	
	
	public Cliente() {
	}
	
	public Cliente(int codigo, int data, Pessoa pessoa) {
		super();
		this.codigo = codigo;
		this.data = data;
		this.pessoa = pessoa;
	}

	
	/**
	 * @return the codigo
	 */
	public int getCodigo() {
		return codigo;
	}
	/**
	 * @param codigo the codigo to set
	 */
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	/**
	 * @return the data
	 */
	public int getData() {
		return data;
	}
	/**
	 * @param data the data to set
	 */
	public void setData(int data) {
		this.data = data;
	}
	/**
	 * @return the pessoa
	 */
	public Pessoa getPessoa() {
		return pessoa;
	}
	/**
	 * @param pessoa the pessoa to set
	 */
	public void setPessoa(Pessoa pessoa) {
		this.pessoa = pessoa;
	}
	
}
